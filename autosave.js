document.addEventListener('DOMContentLoaded', function () {
    
    let autosaveInterval;
    let autosaveCounter = 0;

    console.log('üîÑ Autosave: Modul wird initialisiert...');

    // Canvas-Daten sammeln
    function collectCanvasDataForAutosave() {
        console.log('üñºÔ∏è Autosave: Canvas-Daten werden gesammelt...');
        const canvasData = {};
        const canvasElements = document.querySelectorAll('canvas[id*="signature-canvas"]');
        
        console.log(`üñºÔ∏è Autosave: ${canvasElements.length} Canvas-Elemente gefunden`);
        
        canvasElements.forEach((canvas, index) => {
            if (canvas.id) {
                try {
                    const dataURL = canvas.toDataURL('image/png');
                    if (!isCanvasEmptyForAutosave(canvas)) {
                        canvasData[canvas.id] = dataURL;
                        console.log(`‚úÖ Canvas ${canvas.id} hat Inhalt und wurde erfasst`);
                    } else {
                        console.log(`‚ö™ Canvas ${canvas.id} ist leer`);
                    }
                } catch (error) {
                    console.error(`‚ùå Fehler beim Speichern von Canvas ${canvas.id}:`, error);
                }
            } else {
                console.log(`‚ö†Ô∏è Canvas ${index} hat keine ID`);
            }
        });
        
        console.log(`üñºÔ∏è Autosave: Canvas-Sammlung abgeschlossen. ${Object.keys(canvasData).length} Canvas mit Inhalt`);
        return canvasData;
    }

    // Pr√ºfen ob Canvas leer ist
    function isCanvasEmptyForAutosave(canvas) {
        const context = canvas.getContext('2d');
        const pixelBuffer = new Uint32Array(
            context.getImageData(0, 0, canvas.width, canvas.height).data.buffer
        );
        return !pixelBuffer.some(color => color !== 0);
    }

    // Aktuellen Formularstatus sammeln
    function getCurrentFormState() {
        console.log('üìã Autosave: Formularstatus wird gesammelt...');
        const formData = {};
        let hasData = false;
        let inputCount = 0;
        let filledInputCount = 0;

        // Alle Formularelemente durchgehen
        document.querySelectorAll('input, select, textarea').forEach(element => {
            inputCount++;
            if (element.id) {
                if (element.type === 'checkbox' || element.type === 'radio') {
                    formData[element.id] = element.checked;
                    if (element.checked) {
                        hasData = true;
                        filledInputCount++;
                        console.log(`‚úÖ ${element.type} ${element.id}: ${element.checked}`);
                    }
                } else if (element.type === 'select-one') {
                    formData[element.id] = element.value;
                    if (element.value) {
                        hasData = true;
                        filledInputCount++;
                        console.log(`‚úÖ Select ${element.id}: "${element.value}"`);
                    }
                } else {
                    formData[element.id] = element.value;
                    if (element.value && element.value.trim() !== '') {
                        hasData = true;
                        filledInputCount++;
                        console.log(`‚úÖ Input ${element.id}: "${element.value.substring(0, 20)}${element.value.length > 20 ? '...' : ''}"`);
                    }
                }
            }
        });

        console.log(`üìã Autosave: ${inputCount} Formularelemente gefunden, ${filledInputCount} mit Inhalt`);

        // Radio-Gruppen sammeln
        const radioGroups = {};
        let radioGroupCount = 0;
        document.querySelectorAll('input[type="radio"]').forEach(radio => {
            if (!radioGroups[radio.name]) {
                const checkedRadio = document.querySelector(`input[name="${radio.name}"]:checked`);
                radioGroups[radio.name] = checkedRadio ? checkedRadio.value : '';
                if (checkedRadio) {
                    hasData = true;
                    radioGroupCount++;
                    console.log(`üìª Radio-Gruppe ${radio.name}: "${checkedRadio.value}"`);
                }
            }
        });
        formData.radioGroups = radioGroups;

        console.log(`üìª Autosave: ${Object.keys(radioGroups).length} Radio-Gruppen gefunden, ${radioGroupCount} ausgew√§hlt`);

        // Room-Toggle Status sammeln
        let roomToggleCount = 0;
        document.querySelectorAll('.room-toggle .toggle-option').forEach(option => {
            const room = option.closest('.room-toggle')?.dataset.room;
            if (room && option.classList.contains('active')) {
                const identifier = `room_${room}_toggle`;
                formData[identifier] = option.dataset.value;
                hasData = true;
                roomToggleCount++;
                console.log(`üè† Room-Toggle ${room}: "${option.dataset.value}"`);
            }
        });

        console.log(`üè† Autosave: ${roomToggleCount} Room-Toggles aktiv`);

        // Canvas-Daten sammeln
        const canvasData = collectCanvasDataForAutosave();
        if (Object.keys(canvasData).length > 0) {
            hasData = true;
        }

        const result = {
            formData,
            canvasData,
            hasData,
            timestamp: new Date().toISOString()
        };

        console.log(`üìã Autosave: Formularstatus-Sammlung abgeschlossen. hasData: ${hasData}`);
        return result;
    }

    // Autosave-Namen generieren
    function generateAutosaveName() {
        try {
            const strasse = document.getElementById('strasseeinzug')?.value.trim() || 'Protokoll';
            const now = new Date();
            const dateStr = now.toLocaleDateString('de-DE').replace(/\./g, '-');
            const timeStr = now.toLocaleTimeString('de-DE', { 
                hour: '2-digit', 
                minute: '2-digit', 
                second: '2-digit' 
            }).replace(/:/g, '-');

            autosaveCounter++;
            const name = `AutoSave_${strasse}_${dateStr}_${timeStr}_${autosaveCounter}`.replace(/[/\\?%*:|"<>]/g, '_');
            console.log(`üìù Autosave: Generierter Name: ${name}`);
            return name;
        } catch (e) {
            console.error('‚ùå Autosave: Fehler bei der Namensgenerierung:', e);
            autosaveCounter++;
            return `AutoSave_${Date.now()}_${autosaveCounter}`;
        }
    }

    // Alle gespeicherten Daten abrufen
    function getAllSavesForAutosave() {
        try {
            const saves = localStorage.getItem('formSaves');
            const result = saves ? JSON.parse(saves) : {};
            console.log(`üíæ Autosave: ${Object.keys(result).length} gespeicherte Zust√§nde im localStorage gefunden`);
            return result;
        } catch (e) {
            console.error('‚ùå Autosave: Fehler beim Zugriff auf localStorage:', e);
            return {};
        }
    }

    // Autosave durchf√ºhren - WIRD IMMER AUSGEF√úHRT
    function performAutosave() {
        console.log('');
        console.log('üîÑ ========== AUTOSAVE DURCHGANG GESTARTET ==========');
        console.log('üïê Zeitpunkt:', new Date().toLocaleTimeString());
        
        try {
            const currentState = getCurrentFormState();
            
            console.log(`üìä Daten vorhanden: ${currentState.hasData}`);
            
            // IMMER SPEICHERN - egal ob Daten vorhanden sind oder nicht
            console.log('üíæ SPEICHERUNG WIRD DURCHGEF√úHRT...');
            
            const saveName = generateAutosaveName();
            const allSaves = getAllSavesForAutosave();

            const saveData = {
                data: currentState.formData,
                canvasData: currentState.canvasData,
                timestamp: new Date().toISOString(),
                isAutosave: true
            };

            allSaves[saveName] = saveData;
            
            console.log('üíæ Speichere in localStorage...');
            localStorage.setItem('formSaves', JSON.stringify(allSaves));
            console.log('‚úÖ In localStorage gespeichert');
            
            console.log('üéâ ‚úÖ AUTOSAVE ERFOLGREICH GESPEICHERT! ‚úÖ');
            console.log(`üìÅ Name: ${saveName}`);
            console.log(`üìä Formulardaten: ${Object.keys(currentState.formData).length} Felder`);
            console.log(`üñºÔ∏è Canvas-Daten: ${Object.keys(currentState.canvasData).length} Canvas`);
            console.log(`üïê Timestamp: ${saveData.timestamp}`);
            console.log(`üìà Autosave-Counter: ${autosaveCounter}`);
            console.log(`üì¶ Gesamt gespeicherte Zust√§nde: ${Object.keys(allSaves).length}`);

        } catch (error) {
            console.error('‚ùå FEHLER beim automatischen Speichern:', error);
            console.error('‚ùå Stack Trace:', error.stack);
        }
        
        console.log('üîÑ ========== AUTOSAVE DURCHGANG BEENDET ==========');
        console.log('');
    }

    // Autosave starten - NIEMALS STOPPEN
    function startAutosave() {
        console.log('üöÄ Autosave: Startvorbereitung...');
        
        if (autosaveInterval) {
            console.log('‚èπÔ∏è Vorheriges Interval wird gestoppt');
            clearInterval(autosaveInterval);
        }
        
        // Erstes Autosave sofort ausf√ºhren
        console.log('üé¨ ERSTES AUTOSAVE WIRD JETZT AUSGEF√úHRT');
        performAutosave();
        
        // Alle 10 Sekunden Autosave - NIEMALS STOPPEN
        console.log('‚è∞ Interval wird auf 5 Sekunden gesetzt...');
        autosaveInterval = setInterval(() => {
            console.log('‚è∞ 5-Sekunden-Interval ausgel√∂st');
            performAutosave();
        }, 300000);
        
        console.log('üîÑ Autosave: Automatisches Speichern alle 5 Sekunden gestartet (NIEMALS STOPPEN)');
        console.log('‚úÖ Autosave-Start abgeschlossen');
    }

    // Status abfragen
    function getAutosaveStatus() {
        const status = {
            intervalId: autosaveInterval,
            counter: autosaveCounter,
            isRunning: !!autosaveInterval,
            nextSaveIn: '5 Sekunden'
        };
        console.log('üìä Autosave Status:', status);
        return status;
    }

    // Manuelles Autosave
    function performManualAutosave() {
        console.log('üîß Manuelles Autosave angefordert');
        performAutosave();
    }

    // Alle Autosaves anzeigen
    function showAllAutosaves() {
        try {
            const allSaves = getAllSavesForAutosave();
            const autosaves = Object.entries(allSaves).filter(([name, data]) => data && data.isAutosave);
            
            console.log('üìã ========== ALLE AUTOSAVES ==========');
            console.log(`üìä Gesamt: ${autosaves.length} Autosaves`);
            
            autosaves.forEach(([name, data], index) => {
                console.log(`${index + 1}. ${name} - ${new Date(data.timestamp).toLocaleString()}`);
            });
            
            console.log('üìã ===================================');
            return autosaves;
        } catch (error) {
            console.error('‚ùå Fehler beim Anzeigen der Autosaves:', error);
            return [];
        }
    }

    // Globale Funktionen verf√ºgbar machen
    window.autosaveModule = {
        status: getAutosaveStatus,
        performNow: performManualAutosave,
        showAll: showAllAutosaves
    };

    // Autosave automatisch beim Laden der Seite starten - NIEMALS STOPPEN
    console.log('üöÄ Autosave wird automatisch gestartet und l√§uft NIEMALS STOPPEN...');
    startAutosave();

    console.log('üìÅ Autosave-Modul vollst√§ndig geladen');
    console.log('üîß Verf√ºgbare Funktionen: window.autosaveModule');
    console.log('üîß Status mit: window.autosaveModule.status()');
    console.log('üîß Manuell ausf√ºhren: window.autosaveModule.performNow()');
    console.log('üîß Alle anzeigen: window.autosaveModule.showAll()');
});